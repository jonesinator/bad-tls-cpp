// --- Constexpr test ---

consteval bool test_block_constexpr() {
    std::array<uint8_t, 32> key{};
    std::array<uint8_t, 12> nonce{};
    auto block = chacha20_block(key, nonce, 0);
    return block[0] == 0x76 && block[1] == 0xb8 && block[2] == 0xe0 && block[3] == 0xad;
}

static_assert(test_block_constexpr(), "ChaCha20 block function failed at compile time");

