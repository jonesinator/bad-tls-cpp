#include <crypto/chacha20.hpp>
#include <cassert>
#include <cstdint>

// --- RFC 7539 Section 2.1.1: Quarter Round ---

void test_quarter_round() {
    uint32_t a = 0x11111111, b = 0x01020304, c = 0x9b8d6f43, d = 0x01234567;
    chacha20_detail::quarter_round(a, b, c, d);
    assert(a == 0xea2a92f4);
    assert(b == 0xcb1cf8ce);
    assert(c == 0x4581472e);
    assert(d == 0x5881c4bb);
}

// --- RFC 7539 Section 2.3.2: Block Function ---

void test_block_function() {
    // Key: 00 01 02 ... 1f
    std::array<uint8_t, 32> key{};
    for (int i = 0; i < 32; ++i) key[i] = static_cast<uint8_t>(i);

    // Nonce: 00 00 00 09 00 00 00 4a 00 00 00 00
    std::array<uint8_t, 12> nonce = {0x00, 0x00, 0x00, 0x09, 0x00, 0x00, 0x00, 0x4a, 0x00, 0x00, 0x00, 0x00};

    auto block = chacha20_block(key, nonce, 1);

    // Expected first 16 bytes: 10 f1 e7 e4 d1 3b 59 15 50 0f dd 1f a3 20 71 c4
    assert(block[0]  == 0x10 && block[1]  == 0xf1 && block[2]  == 0xe7 && block[3]  == 0xe4);
    assert(block[4]  == 0xd1 && block[5]  == 0x3b && block[6]  == 0x59 && block[7]  == 0x15);
    assert(block[8]  == 0x50 && block[9]  == 0x0f && block[10] == 0xdd && block[11] == 0x1f);
    assert(block[12] == 0xa3 && block[13] == 0x20 && block[14] == 0x71 && block[15] == 0xc4);

    // Expected last 16 bytes: b5 12 9c d1 de 16 4e b9 cb d0 83 e8 a2 50 3c 4e
    assert(block[48] == 0xb5 && block[49] == 0x12 && block[50] == 0x9c && block[51] == 0xd1);
    assert(block[60] == 0xa2 && block[61] == 0x50 && block[62] == 0x3c && block[63] == 0x4e);
}

// --- Appendix A.1 Test Vector #1: all-zeros key/nonce ---

void test_block_all_zeros() {
    std::array<uint8_t, 32> key{};
    std::array<uint8_t, 12> nonce{};

    auto block = chacha20_block(key, nonce, 0);

    // Expected first 16 bytes: 76 b8 e0 ad a0 f1 3d 90 40 5d 6a e5 53 86 bd 28
    assert(block[0]  == 0x76 && block[1]  == 0xb8 && block[2]  == 0xe0 && block[3]  == 0xad);
    assert(block[4]  == 0xa0 && block[5]  == 0xf1 && block[6]  == 0x3d && block[7]  == 0x90);
    assert(block[12] == 0x53 && block[13] == 0x86 && block[14] == 0xbd && block[15] == 0x28);

    // Expected last 4 bytes: ee 65 86
    assert(block[61] == 0xee && block[62] == 0x65 && block[63] == 0x86);
}

