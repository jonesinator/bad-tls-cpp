#include <crypto/poly1305.hpp>
#include <cassert>
#include <cstdint>

// --- RFC 7539 Section 2.5.2: Poly1305 MAC ---

void test_poly1305_rfc7539() {
    // Key (32 bytes)
    const std::array<uint8_t, 32> key = {
        0x85, 0xd6, 0xbe, 0x78, 0x57, 0x55, 0x6d, 0x33, 0x7f, 0x44, 0x52, 0xfe, 0x42, 0xd5, 0x06, 0xa8,
        0x01, 0x03, 0x80, 0x8a, 0xfb, 0x0d, 0xb2, 0xfd, 0x4a, 0xbf, 0xf6, 0xaf, 0x41, 0x49, 0xf5, 0x1b
    };

    // Message: "Cryptographic Forum Research Group" (34 bytes)
    const uint8_t message[] = {
        0x43, 0x72, 0x79, 0x70, 0x74, 0x6f, 0x67, 0x72, 0x61, 0x70, 0x68, 0x69, 0x63, 0x20, 0x46, 0x6f,
        0x72, 0x75, 0x6d, 0x20, 0x52, 0x65, 0x73, 0x65, 0x61, 0x72, 0x63, 0x68, 0x20, 0x47, 0x72, 0x6f,
        0x75, 0x70
    };

    // Expected tag
    const std::array<uint8_t, 16> expected = {
        0xa8, 0x06, 0x1d, 0xc1, 0x30, 0x51, 0x36, 0xc6, 0xc2, 0x2b, 0x8b, 0xaf, 0x0c, 0x01, 0x27, 0xa9
    };

    auto tag = poly1305_mac(key, message);
    assert(tag == expected);
}

// --- Incremental interface test ---

void test_poly1305_incremental() {
    const std::array<uint8_t, 32> key = {
        0x85, 0xd6, 0xbe, 0x78, 0x57, 0x55, 0x6d, 0x33, 0x7f, 0x44, 0x52, 0xfe, 0x42, 0xd5, 0x06, 0xa8,
        0x01, 0x03, 0x80, 0x8a, 0xfb, 0x0d, 0xb2, 0xfd, 0x4a, 0xbf, 0xf6, 0xaf, 0x41, 0x49, 0xf5, 0x1b
    };

    const uint8_t message[] = {
        0x43, 0x72, 0x79, 0x70, 0x74, 0x6f, 0x67, 0x72, 0x61, 0x70, 0x68, 0x69, 0x63, 0x20, 0x46, 0x6f,
        0x72, 0x75, 0x6d, 0x20, 0x52, 0x65, 0x73, 0x65, 0x61, 0x72, 0x63, 0x68, 0x20, 0x47, 0x72, 0x6f,
        0x75, 0x70
    };

    const std::array<uint8_t, 16> expected = {
        0xa8, 0x06, 0x1d, 0xc1, 0x30, 0x51, 0x36, 0xc6, 0xc2, 0x2b, 0x8b, 0xaf, 0x0c, 0x01, 0x27, 0xa9
    };

    // Feed in small chunks to exercise the buffering logic
    poly1305_state state;
    state.init(key);
    state.update(std::span<const uint8_t>(message, 10));       // 10 bytes
    state.update(std::span<const uint8_t>(message + 10, 7));   // 7 bytes
    state.update(std::span<const uint8_t>(message + 17, 17));  // 17 bytes (crosses block boundary)
    auto tag = state.finalize();
    assert(tag == expected);
}

// --- Empty message ---

void test_poly1305_empty() {
    // With a zero key and empty message, tag should be all zeros (s = 0, h = 0)
    std::array<uint8_t, 32> key{};
    std::span<const uint8_t> empty{};
    auto tag = poly1305_mac(key, empty);
    for (auto b : tag) assert(b == 0);
}

// --- Constexpr test ---

consteval bool test_poly1305_constexpr() {
    std::array<uint8_t, 32> key = {
        0x85, 0xd6, 0xbe, 0x78, 0x57, 0x55, 0x6d, 0x33, 0x7f, 0x44, 0x52, 0xfe, 0x42, 0xd5, 0x06, 0xa8,
        0x01, 0x03, 0x80, 0x8a, 0xfb, 0x0d, 0xb2, 0xfd, 0x4a, 0xbf, 0xf6, 0xaf, 0x41, 0x49, 0xf5, 0x1b
    };
    uint8_t message[] = {
        0x43, 0x72, 0x79, 0x70, 0x74, 0x6f, 0x67, 0x72, 0x61, 0x70, 0x68, 0x69, 0x63, 0x20, 0x46, 0x6f,
        0x72, 0x75, 0x6d, 0x20, 0x52, 0x65, 0x73, 0x65, 0x61, 0x72, 0x63, 0x68, 0x20, 0x47, 0x72, 0x6f,
        0x75, 0x70
    };
    auto tag = poly1305_mac(key, message);
    return tag[0] == 0xa8 && tag[1] == 0x06 && tag[15] == 0xa9;
}

static_assert(test_poly1305_constexpr(), "Poly1305 RFC 7539 test vector failed at compile time");

int main() {
    test_poly1305_rfc7539();
    test_poly1305_incremental();
    test_poly1305_empty();
    return 0;
}
