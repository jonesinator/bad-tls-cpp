#include <crypto/aes.hpp>
#include <cassert>
#include <cstdint>
#include <array>

// --- FIPS 197 Appendix B: AES-128 ---

void test_aes128_appendix_b() {
    // Key:        2b7e151628aed2a6abf7158809cf4f3c
    // Plaintext:  3243f6a8885a308d313198a2e0370734
    // Ciphertext: 3925841d02dc09fbdc118597196a0b32
    constexpr std::array<uint8_t, 16> key = {
        0x2B,0x7E,0x15,0x16, 0x28,0xAE,0xD2,0xA6,
        0xAB,0xF7,0x15,0x88, 0x09,0xCF,0x4F,0x3C
    };
    constexpr std::array<uint8_t, 16> pt = {
        0x32,0x43,0xF6,0xA8, 0x88,0x5A,0x30,0x8D,
        0x31,0x31,0x98,0xA2, 0xE0,0x37,0x07,0x34
    };
    constexpr std::array<uint8_t, 16> expected = {
        0x39,0x25,0x84,0x1D, 0x02,0xDC,0x09,0xFB,
        0xDC,0x11,0x85,0x97, 0x19,0x6A,0x0B,0x32
    };

    aes128 aes;
    aes.init(key);
    auto ct = aes.encrypt_block(pt);
    assert(ct == expected);
    auto rt = aes.decrypt_block(ct);
    assert(rt == pt);
}

