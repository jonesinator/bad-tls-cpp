// --- Compile-time test ---

void test_constexpr() {
    constexpr std::array<uint8_t, 16> key = {
        0x00,0x01,0x02,0x03, 0x04,0x05,0x06,0x07,
        0x08,0x09,0x0A,0x0B, 0x0C,0x0D,0x0E,0x0F
    };
    constexpr std::array<uint8_t, 16> pt = {
        0x00,0x11,0x22,0x33, 0x44,0x55,0x66,0x77,
        0x88,0x99,0xAA,0xBB, 0xCC,0xDD,0xEE,0xFF
    };
    constexpr auto ct = aes_encrypt<128>(key, pt);
    static_assert(ct[0] == 0x69 && ct[1] == 0xC4 && ct[2] == 0xE0 && ct[3] == 0xD8);
    static_assert(ct[12] == 0x70 && ct[13] == 0xB4 && ct[14] == 0xC5 && ct[15] == 0x5A);

    constexpr auto rt = aes_decrypt<128>(key, ct);
    static_assert(rt == pt);
}

int main() {
    test_aes128_appendix_b();
    test_aes128_appendix_c();
    test_aes192_appendix_c();
    test_aes256_appendix_c();
    test_constexpr();
    return 0;
}
