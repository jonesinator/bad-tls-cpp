// --- Decrypt with wrong tag returns nullopt ---

void test_decrypt_rejects_bad_tag() {
    constexpr std::array<uint8_t, 16> key{};
    constexpr std::array<uint8_t, 12> iv{};
    constexpr std::array<uint8_t, 16> pt{};
    constexpr std::array<uint8_t, 0> aad{};

    auto result = gcm_encrypt<aes128, 16>(key, iv, pt, aad);

    // Corrupt the tag
    auto bad_tag = result.tag;
    bad_tag[0] ^= 0x01;

    auto decrypted = gcm_decrypt<aes128, 16>(key, iv, result.ciphertext, aad, bad_tag);
    assert(!decrypted.has_value());
}

// --- Compile-time test (Case 1: tag-only) ---

void test_constexpr() {
    constexpr std::array<uint8_t, 16> key{};
    constexpr std::array<uint8_t, 12> iv{};
    constexpr std::array<uint8_t, 0> pt{};
    constexpr std::array<uint8_t, 0> aad{};

    constexpr auto result = gcm_encrypt<aes128, 0>(key, iv, pt, aad);
    static_assert(result.tag[0] == 0x58 && result.tag[1] == 0xE2);
    static_assert(result.tag[14] == 0x45 && result.tag[15] == 0x5A);

    constexpr auto decrypted = gcm_decrypt<aes128, 0>(key, iv, result.ciphertext, aad, result.tag);
    static_assert(decrypted.has_value());
}

