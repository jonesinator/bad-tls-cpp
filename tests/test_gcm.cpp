// --- GCM spec Appendix B, Test Case 1: AES-128, empty PT, empty AAD ---

void test_gcm_case1() {
    constexpr std::array<uint8_t, 16> key{};
    constexpr std::array<uint8_t, 12> iv{};
    constexpr std::array<uint8_t, 0> pt{};
    constexpr std::array<uint8_t, 0> aad{};
    // T = 58e2fccefa7e3061367f1d57a4e7455a
    constexpr std::array<uint8_t, 16> expected_tag = {
        0x58,0xE2,0xFC,0xCE, 0xFA,0x7E,0x30,0x61,
        0x36,0x7F,0x1D,0x57, 0xA4,0xE7,0x45,0x5A
    };

    auto result = gcm_encrypt<aes128, 0>(key, iv, pt, aad);
    assert(result.tag == expected_tag);

    auto decrypted = gcm_decrypt<aes128, 0>(key, iv, result.ciphertext, aad, result.tag);
    assert(decrypted.has_value());
}

